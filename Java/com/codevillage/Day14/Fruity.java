package com.codevillage.Day14;

public class Fruity{

    private String name;
    private String color;
    private boolean hasSeeds;


    public String getname(){
        return name;
    }

    public void  setname(){
        this.name=name;
    }

    public String getcolor(){
        return color;
    }

    public void  setcolor(){
        this.color=color;
    }

    public boolean getHasSeeds(){
        return hasSeeds;
    }

    public void  setHasSeeds(){
        this.hasSeeds=hasSeeds;
    }

}
